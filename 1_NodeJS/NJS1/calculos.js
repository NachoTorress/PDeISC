//Creación de funciones para realizar operaciones matemáticas básicas
function suma(a, b) {
    return a + b;
}
function resta(a, b) {
    return a - b;
}
function multiplicacion(a, b) {
    return a * b;
}
function division(a, b) {
    return a / b;
}
// Exportación de las funciones para su uso en otros archivos
export {suma, resta, multiplicacion, division};